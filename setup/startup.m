%% 1. Add model directory to path в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
repoRoot = fileparts(fileparts(mfilename('fullpath')));
addpath(fullfile(repoRoot, 'model'));
addpath(fullfile(repoRoot, 'validation'));
fprintf('[1/4] Paths added: model, validation\n');

%% 2. Load cardiac parameters into base workspace в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
run(fullfile(repoRoot, 'model', 'cardiac_params.m'));
fprintf('[2/4] Cardiac parameters loaded\n');

%% 3. Initialize the Simulink Agentic Toolkit (MCP bridge) в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
% Requires prior toolkit installation:
%   setupAgenticToolkit("install")  [run once, outside this script]
toolkitPath = fullfile(userpath, '..', '.matlab', 'agentic-toolkits', 'simulink');
if exist(toolkitPath, 'dir')
    addpath(toolkitPath);
    try
        satk_initialize;
        fprintf('[3/4] Simulink Agentic Toolkit initialized (MCP bridge active)\n');
    catch ME
        fprintf('[3/4] WARNING: satk_initialize failed вЂ” %s\n', ME.message);
        fprintf('      Ensure toolkit is installed: setupAgenticToolkit("install")\n');
    end
else
    fprintf('[3/4] WARNING: Simulink Agentic Toolkit not found at expected path.\n');
    fprintf('      Install with: setupAgenticToolkit("install")\n');
end

%% 4. Build or reload the model в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
mdlFile = fullfile(repoRoot, 'model', 'CardiacDigitalTwin.slx');
if ~exist(mdlFile, 'file')
    fprintf('[4/4] Model not found вЂ” building CardiacDigitalTwin.slx ...\n');
    run(fullfile(repoRoot, 'model', 'create_cardiac_model.m'));
else
    fprintf('[4/4] Model found вЂ” opening CardiacDigitalTwin.slx\n');
    load_system(mdlFile);
    open_system(mdlFile);
end

